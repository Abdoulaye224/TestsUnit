package main;

public class Calculatrice {

    public int add(int a, int b){
        int result = a + b;
        return result;
    }

    public int sous(int a, int b){
        int result = a - b;
        return result;
    }
    public int mul(int a, int b){
        int result = a * b;
        return result;
    }

    public int div(int a, int b){
        int result = a / b;

        return result;
    }
}
